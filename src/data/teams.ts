export function getTeam(slug: string): TeamData | undefined {
  return teams[slug];
}

export function getTeamsByConference(conference: "east" | "west"): TeamData[] {
  return Object.values(teams)
    .filter((t) => t.conference === conference)
    .sort((a, b) => a.seed - b.seed);
}

export function getAllTeamSlugs(): string[] {
  return Object.keys(teams);
}

export function getAdjacentTeams(slug: string): {
  prev: TeamData | null;
  next: TeamData | null;
} {
  const team = getTeam(slug);
  if (!team) return { prev: null, next: null };

  const confTeams = getTeamsByConference(team.conference);
  const idx = confTeams.findIndex((t) => t.slug === slug);

  return {
    prev: idx > 0 ? confTeams[idx - 1] : null,
    next: idx < confTeams.length - 1 ? confTeams[idx + 1] : null,
  };
}
